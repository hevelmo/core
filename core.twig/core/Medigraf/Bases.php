<?php

//namespace Medigraf;

class Bases {
    
    //---------------------------------------------------------------------------------------------
    //---------------------------------------- PROPERTIES -----------------------------------------
    //---------------------------------------------------------------------------------------------
    
    private $constants;
    
    //---------------------------------------------------------------------------------------------
    //---------------------------------------- CONSTRUCT ------------------------------------------
    //---------------------------------------------------------------------------------------------
    
    function __construct() {
        $this->constants = array(
            "_host" => _HOST,
            "_sitio" => _SITIO,
            "_intranet" => _INTRANET,
            "_admin" => _ADMIN,
            "_anio" => date("o")
        );
    }
    
    //---------------------------------------------------------------------------------------------
    //----------------------------------------- GETTERS -------------------------------------------
    //---------------------------------------------------------------------------------------------
    
    public function getConstants() {
        return $this->constants;
    }
    
}
